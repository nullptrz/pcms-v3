    /*Method to search for a specific row in a table*/
    private void filter(String query) {
        TableRowSorter<DefaultTableModel> searchRow = new TableRowSorter<DefaultTableModel>(model); //initialize the rowsorter
        jTable1.setRowSorter(searchRow); //Apply the row sorter to the table
        searchRow.setRowFilter(RowFilter.regexFilter(query)); //Determine that query is the input to sort on (Regex = regular expression)

    }


