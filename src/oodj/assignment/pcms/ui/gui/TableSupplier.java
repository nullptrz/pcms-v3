    private void jEditButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEditButtonActionPerformed
        try {
            int row = jTable1.getSelectedRow();
            String id = (String) model.getValueAt(row, 0);
            String name = (String) model.getValueAt(row, 1);
            String status = (String) model.getValueAt(row, 2);
            EditSupplier es = new EditSupplier(id, name, status);
            es.setLocationRelativeTo(null);
            es.setVisible(true);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane,
                    "Pick a supplier info to edit");
        }


    }//GEN-LAST:event_jEditButtonActionPerformed


